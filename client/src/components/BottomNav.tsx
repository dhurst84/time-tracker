import { NavLink } from 'react-router-dom'
import { useAuthStore } from '../store/authStore'

export default function BottomNav() {
  const user = useAuthStore((s) => s.user)
  const isAdmin = user?.role === 'admin'

  const baseTabs = [
    { to: '/today', label: 'Today', icon: ClockIcon },
    { to: '/clients', label: 'Clients', icon: UsersIcon },
    { to: '/client-groups', label: 'Groups', icon: GroupIcon },
    { to: '/tasks', label: 'Tasks', icon: TasksIcon },
    { to: '/reports', label: 'Reports', icon: ChartIcon },
  ]

  const adminTabs = [
    { to: '/team', label: 'Team', icon: TeamIcon },
    { to: '/settings', label: 'Settings', icon: SettingsIcon },
  ]

  const memberTabs = [
    { to: '/account', label: 'Account', icon: AccountIcon },
  ]

  const tabs = isAdmin
    ? [...baseTabs, ...adminTabs]
    : [...baseTabs, ...memberTabs]

  return (
    <nav className="lg:hidden fixed bottom-0 left-0 right-0 bg-white border-t border-stone-200 flex z-40">
      {tabs.map(({ to, label, icon: Icon }) => (
        <NavLink
          key={to}
          to={to}
          className={({ isActive }) =>
            `flex-1 flex flex-col items-center gap-0.5 py-2 text-xs font-medium transition-colors ${
              isActive ? 'text-teal-600' : 'text-stone-500'
            }`
          }
        >
          <Icon className="w-5 h-5" />
          <span className="text-[10px] leading-tight">{label}</span>
        </NavLink>
      ))}
    </nav>
  )
}

